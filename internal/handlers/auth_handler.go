package handlers

import (
	"encoding/json"
	"net/http"

	"electric-circuit-web/server/internal/controllers"
)

// AuthHandler handles authentication-related HTTP requests
type AuthHandler struct {
	authController *controllers.AuthController
}

// NewAuthHandler creates a new auth handler
func NewAuthHandler(authController *controllers.AuthController) *AuthHandler {
	return &AuthHandler{
		authController: authController,
	}
}

// HandleVerifyToken handles token verification requests
func (h *AuthHandler) HandleVerifyToken(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		return
	}

	var req controllers.AuthRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, "Invalid request body", http.StatusBadRequest)
		return
	}

	resp, err := h.authController.VerifyToken(r.Context(), &req)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(resp)
}

